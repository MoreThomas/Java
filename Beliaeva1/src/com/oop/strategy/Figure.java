package com.oop.strategy;

public abstract class Figure {
	private int a, b;	
	
	public int getA() {
		return a;
	}

	public void setA(int a) {
		this.a = a;
	}

	public int getB() {
		return b;
	}

	public void setB(int b) {
		this.b = b;
	}

	public abstract String toString(); 
}
